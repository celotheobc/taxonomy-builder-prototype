const FACILITY_FIELD_BY_ATTRIBUTE = {
  country: 'country',
  city: 'city',
  status: 'status',
  'business-unit': 'businessUnit',
  'opened-on': 'openedOn',
  'facility-type': 'facilityType',
  'facility-name': 'name',
  'facility-id': 'id',
};

function aggregateFacilityAttributeValues(rows) {
  const result = {};
  Object.entries(FACILITY_FIELD_BY_ATTRIBUTE).forEach(([attributeId, field]) => {
    const counts = new Map();
    rows.forEach((row) => {
      counts.set(row[field], (counts.get(row[field]) ?? 0) + 1);
    });
    result[attributeId] = [...counts.entries()]
      .map(([value, recordCount]) => ({ value, recordCount }))
      .sort((a, b) => a.value.localeCompare(b.value));
  });
  return result;
}

export const FACILITY_ATTRIBUTE_VALUES = aggregateFacilityAttributeValues(FACILITY_ROWS);
